fav_color = 'pink'

case fav_color
when 'red'
    puts "Red likes fire!"
when 'orange'
    puts "Orange like, well... an orange."
when 'yellow'
    puts "Yellow daffodils are so pretty in the spring!"
when 'green'
    puts "Have you been to the Emerald City in Oz?"
when 'blue'
    puts "Blue like the sky!"
when 'purple'
    puts "Purple plums are the tastiest."
else 
    puts "Hmmm, well I don't know what that color is!"
end

weather = 'snow'
case weather 
when 'sunny'
    puts "Go outside right now!"
when 'rainy'
    puts "Take your umbrella"
when 'foggy'
    puts "Drive carefully"
when 'cloudy'
    puts "Today is a gloomy day!"
when 'snow'
    puts "Drink some hot chocolate and stay under your blankets"
else 
    puts "I don't know what the weather is. Check your app."
end